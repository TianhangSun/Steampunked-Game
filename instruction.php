<p>A player always has five available pipes. Each turn they can select one of the pipes using the radio button next to it. Then they can rotate it as many times as they like, then click on an open space that extends their pipe. They can only add a pipe where it will connect to their pipes. No other locations will work. This is, in fact, all of the locations where the player's pipe currently leaks. </p>
<p>If they press discard, the selected pipe section is discarded and the turn ends.</p>
<p>Give up indicates that a player gives up. It should jump to a page that indicates the other player is the winner.</p>
<p>If player has a completely connected pipe, they press Open Valve to turn on the steam. If there is a leak in their pipe, they lose. If they have successfully connected the source to the destination, they win.</p>
